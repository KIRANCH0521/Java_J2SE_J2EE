package Payroll;

public class Employee {
	int id;
	String name;
	String addr;
	int deptid;
	double basicsal;
	public Employee(int id, String name, String addr, int deptid,
			double basicsal) {
		super();
		this.id = id;
		this.name = name;
		this.addr = addr;
		this.deptid = deptid;
		this.basicsal = basicsal;
	}
	
	
}
